def build_keywords(spec: CategorySpec, transcript: str) -> str:
    keywords = list(spec.keywords)
    if "보더콜리" in transcript:
        keywords.append("강아지")
    if "창고" in transcript:
        keywords.append("보관")
    return ", ".join(dict.fromkeys(keywords))



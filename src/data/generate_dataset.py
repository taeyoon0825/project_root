def generate_dataset(total_items: int = 100) -> pd.DataFrame:
    ensure_project_dirs()
    rng = random.Random(RANDOM_SEED)
    categories = list(CATEGORY_SPECS.keys())
    records: list[dict[str, str]] = []

    for index in range(1, total_items + 1):
        category = categories[(index - 1) % len(categories)]
        spec = CATEGORY_SPECS[category]
        transcript, title = build_long_transcript(spec, index, rng)
        stt_transcript = make_stt_variant(transcript, rng)
        processed_text = normalize_text(transcript.lower())
        keywords = build_keywords(spec, transcript)

        file_name = f"doc_{index:03d}.txt"
        raw_path = RAW_DIR / file_name
        processed_path = PROCESSED_DIR / file_name

        raw_path.write_text(transcript, encoding="utf-8")
        processed_path.write_text(processed_text, encoding="utf-8")

        records.append(
            {
                "id": f"DOC-{index:03d}",
                "category": category,
                "title": title,
                "file_name": file_name,
                "file_path": str(raw_path.resolve()),
                "processed_txt_path": str(processed_path.resolve()),
                "original_transcript": transcript,
                "stt_transcript": stt_transcript,
                "keywords": keywords,
                "tts_text": transcript,
                "audio_file_name": "",
                "audio_file_path": "",
                "stt_txt_path": "",
                "tts_provider": "",
                "stt_model_name": "",
            }
        )

    metadata = pd.DataFrame(records)
    save_dataframe(DEFAULT_METADATA_CSV, metadata)
    save_dataframe(DEFAULT_QUERYSET_CSV, generate_queries())
    return metadata


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate long-form Korean dummy transcript dataset.")
    parser.add_argument("--total-items", type=int, default=100, help="Number of dummy transcript records to create.")
    args = parser.parse_args()

    metadata = generate_dataset(total_items=args.total_items)
    print(f"Generated dataset with {len(metadata)} records")
    print(f"Metadata CSV: {DEFAULT_METADATA_CSV}")
    print(f"Evaluation query CSV: {DEFAULT_QUERYSET_CSV}")


if __name__ == "__main__":
    main()
